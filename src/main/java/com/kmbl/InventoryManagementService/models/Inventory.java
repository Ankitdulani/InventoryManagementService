package com.kmbl.InventoryManagementService.models;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.util.List;

@Data
@NoArgsConstructor
@DynamoDBTable(tableName = "inventory")
public class Inventory {

    @DynamoDBAttribute
    List<Integer> pincodes;
    @DynamoDBAutoGeneratedKey
    @DynamoDBHashKey(attributeName = "id")
    private String id;
    @DynamoDBAttribute(attributeName = "sellerId")
    //@DynamoDBIndexRangeKey(globalSecondaryIndexName = "productIdandsellerId-index",attributeName = "productid")
    private String sellerId;
    @DynamoDBAttribute(attributeName = "productId")
    // @DynamoDBIndexHashKey(globalSecondaryIndexName = "productIdandsellerId-index",attributeName = "sellerid")
    private String productId;
    @DynamoDBAttribute
    private int quantity;
    @DynamoDBAttribute
    private double price;

    public String getSellerId() {
        return sellerId;
    }

    public void setSellerId(String sellerId) {
        this.sellerId = sellerId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
